#include "3-calc.h"
/**
 * op_add - add function
 * @a: first num
 * @b: second num
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_mod - mod function
 * @a: first num
 * @b: second num
 * Return: sum
 */
int op_mod(int a, int b)
{
	return (a % b);
}
/**
 * op_div - div function
 * @a: first num
 * @b: second num
 * Return: sum
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mul - mul function
 * @a: first num
 * @b: second num
 * Return: sum
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_sub - sub function
 * @a: first num
 * @b: second num
 * Return: sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}
